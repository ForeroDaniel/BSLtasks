export function quarterOf(month: number): number {
  let result:number;
  if (month > 0 && month < 4){
    result = 1;
  }else if (month > 3 && month < 7){
    result = 2;
  }else if (month > 6 && month < 10){
    result = 3;
  }else { result = 4; }
  
  return result
}
                                     
                                     
                                     
